'use strict'

const moment = require('moment')

const user = responseBody => {
  return `
    name: ${responseBody.name},
    company: ${responseBody.company || ''},
    blog: ${responseBody.blog || ''},
    followers: ${responseBody.followers},
    following: ${responseBody.following},
    publicRepos: ${responseBody.public_repos},
    publicGits: ${responseBody.public_gists},
    created: ${moment(responseBody.created_at).format('YYYY/MM/DD')},
    reposUrl: ${responseBody.repos_url}
  `
}

module.exports = {user}
